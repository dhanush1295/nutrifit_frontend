export function matchesDiet(itemDiet, userDiet) {
  if (userDiet === 'vegan') return itemDiet === 'vegan';
  if (userDiet === 'pureVegetarian') return itemDiet === 'pureVegetarian' || itemDiet === 'vegan';
  return true; // nonVegetarian
}

export function getSafeFoods(conditions, diet) {
    const conditionArray = Array.from(conditions);
    return globalFoodDatabase.filter(item => {
        const avoids = item.avoidFor.some(cond => conditionArray.includes(cond));
        return !avoids && matchesDiet(item.diet, diet);
    });
}

export function getAvoidFoods(conditions, diet) {
    const conditionArray = Array.from(conditions);
    return globalFoodDatabase.filter(item => {
        return item.avoidFor.some(cond => conditionArray.includes(cond));
    });
}

export function generateDefaultMeals(conditions, diet) {
  const safe = getSafeFoods(conditions, diet);
  const getMeal = (type, fallback) => {
    const opts = safe.filter(f => f.mealTimes.includes(type));
    if (opts.length > 0) return opts[Math.floor(Math.random() * opts.length)];
    return globalFoodDatabase.find(f => f.name === fallback);
  };

  const breakfast = getMeal("Breakfast", "Vegetable Poha");
  const lunch = getMeal("Lunch", "Dal Tadka with Brown Rice");
  const dinner = getMeal("Dinner", "Moong Dal Khichdi");
  const snack = getMeal("Snack", "Roasted Chana");

  localStorage.setItem('currentBreakfast', JSON.stringify(breakfast));
  localStorage.setItem('currentLunch', JSON.stringify(lunch));
  localStorage.setItem('currentDinner', JSON.stringify(dinner));
  localStorage.setItem('currentSnack', JSON.stringify(snack));

  const totalCals = breakfast.calories + lunch.calories + snack.calories + dinner.calories;
  localStorage.setItem('caloriesEaten', totalCals.toString());
}

export function loadMeal(key) {
  const str = localStorage.getItem(`current${key}`);
  return str ? JSON.parse(str) : null;
}

export function getSwapMeal(mealType, calorieLimit, conditions, diet, excludingName = "") {
  const safe = getSafeFoods(conditions, diet);
  const candidates = safe.filter(item => 
    item.mealTimes.includes(mealType) && 
    item.calories <= calorieLimit && 
    item.name !== excludingName
  );
  if (candidates.length === 0) return null;
  return candidates[Math.floor(Math.random() * candidates.length)];
}
